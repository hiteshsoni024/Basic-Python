#here we are learn about some mathematical functions which are expressed by the help of math module 
import math
pi=3.14
#round round down the given argumented variable to its nearest integer
print(round(pi))
#ceil function used with math module and it will round up the variable to the nearest integer
print(math.ceil(pi))
#floor is also use to round down the variable to the nearest integer by using math module
print(math.floor(pi))
#gives absolute value
print(abs(pi))
#Power function
print(pow(pi,2))
#square root 
print(math.sqrt(420))
#find maximum among given value
print(max(33,45,75))
#find minimum value among given value
print(min(21,23,55))
