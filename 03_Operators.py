#arithmatic operators
x=2
y=3

'''Arithmatic operator'''
# Addition
print("sum :",x+y)
#Subtraction
print("subtraction: ",x-y)
#multiply
print("multiply: ",x*y)
#Devide
print("Devide: ",x/y)
#reminder
print("reminder: ",x%y)
#Exponential
print("Exponential: ",x**y)
# Floor Division
print("Floor division: ",x//y)

'''Realtional operators'''
a=4
b=3
c=4
d=9

#Equality operator
print(a==b)
print(a==c)
print(a==d)
print(a!=d)

#less than, greater than, less than equal , greater than equal 
print(a>b)
print(b>c)
print(a>=b)
print(a<=c)

'''Logiacal Operator'''
#And
print(True and True)
print(True and False)
print(False and True)
print(False and False)
#OR
print(True or True)
print(True or False)
print(False or True)
print(False or False)
#NOT
print(not True)
print(not False)

'''Bitwise operator'''
#bitwise and 
print(5 & 4)
#bitwise or 
print(3 | 5)
#bitwise not
print(~5)
print(~10)
#bitwise xor
print(3^4)
#bitwise right shift with 2 places
print(6>>2)
#bitwise left shift with 3 places
print(5<<3)